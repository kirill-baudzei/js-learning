// решение
const someString = 'This is some strange string';

function reverse(str) {
    if (typeof str !== 'string') {
        return "Ошибка!";
    }

    let reversedStr = '';
    for (let i = str.length - 1; i >= 0; i--) { // Проходим по строке с конца (с последнего символа) к началу, добавляя символы в новую строку.
        reversedStr += str[i];
    }
    return reversedStr;
}
